    public GameMap(Game game){
        try {
            this.tiles = new Tiles(this.tilePath);
        } catch(IOException e){
            e.printStackTrace();
        }
        this.spritesheet = tiles.getImage();
        this.game = game;
    }

    public void draw(Graphics g2d){
        for(int i = 0; i < this.map.length; i++){
            for(int j = 0; j < this.map[i].length; j++){
                if(this.map[i][j] == 0){
                    g2d.drawImage(this.tiles.getTiles(0),(int)((63 * j)-this.game.getGameCamera().getXOffset()),(int)(((i*63)) - this.game.getGameCamera().getYOffset()),null);
                    // g2d.drawRect((int)((63 * j)-this.game.getGameCamera().getXOffset()),(int)(((i*63)) - this.game.getGameCamera().getYOffset()), 63,63);
                } else {
                    g2d.drawImage(this.tiles.getTiles(1),(int)((63 * j)-this.game.getGameCamera().getXOffset()),(int)(((i*63)) - this.game.getGameCamera().getYOffset()),null);
                }
            }
        }
    }

    public int getWidthInTiles(){
        return this.map[0].length;
    }

    public int getHeightInTiles(){
        return this.map.length;
    }

    public int[][] getMap(){
        return this.map;
    }

